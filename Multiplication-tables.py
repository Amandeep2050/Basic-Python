# Printing table of a given number using while loop.

# Taking a number as input
n = int(input("Enter a number: "))
i = 1 # iteration variable

# Running While loop for 10 times
while i<=10:                    # Condition
    print(n ,"X",i,"=", n*i)    # Print table
    i+=1                        # Update condtion

print("Thankyou!")              # End of the code.