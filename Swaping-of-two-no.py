# Using third variable for swaping.

# taking two number as input
a = input("Enter a: ")
b = input("Enter b: ")

# performing operation to swap
# let a = 2 , b = 3
c = a # storing a into c        c = 2
a = b # giving b's value to a   a = 3
b = c # giving c's value to b   b = 2

# Printing the result
print("After swaping:- ")
print("a = ", a)
print("b = ", b)
